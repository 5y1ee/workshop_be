CATEGORIES: list[str] = list(dict.fromkeys(q.category for q in QUIZ_QUESTIONS))


def by_category(category: str) -> list[QuizQuestion]:
    """주어진 카테고리의 문제만 반환."""
    return [q for q in QUIZ_QUESTIONS if q.category == category]


def category_counts() -> dict[str, int]:
    """카테고리별 문제 수."""
    counts: dict[str, int] = {}
    for q in QUIZ_QUESTIONS:
        counts[q.category] = counts.get(q.category, 0) + 1
    return counts


def validate() -> list[str]:
    """데이터 무결성 검사. 문제가 있으면 에러 메시지 리스트를 반환한다 (없으면 빈 리스트)."""
    errors: list[str] = []
    for i, q in enumerate(QUIZ_QUESTIONS):
        if len(q.options) < 2:
            errors.append(f"[{i}] '{q.prompt}': 보기가 2개 미만입니다.")
        if len(set(q.options)) != len(q.options):
            errors.append(f"[{i}] '{q.prompt}': 보기에 중복이 있습니다.")
        if q.answer not in q.options:
            errors.append(
                f"[{i}] '{q.prompt}': 정답 '{q.answer}' 가 보기 {q.options} 에 없습니다."
            )
    return errors


if __name__ == "__main__":
    errs = validate()
    if errs:
        print("❌ 데이터 검증 실패:")
        for e in errs:
            print("  -", e)
        raise SystemExit(1)
    print(f"✅ 총 {len(QUIZ_QUESTIONS)} 문제 / {len(CATEGORIES)} 카테고리")
    for cat, cnt in category_counts().items():
        print(f"   - {cat}: {cnt}")
